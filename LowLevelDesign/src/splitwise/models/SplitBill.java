package splitwise.splitwise.models;

public abstract class SplitBill {
    private User user;
    double amount;

    public SplitBill(User user) {
        this.user = user;
    }

    public User getUser() {
        return user;
    }

    public void setUser(User user) {
        this.user = user;
    }

    public double getAmount() {
        return amount;
    }

    public void setAmount(double amount) {
        this.amount = amount;
    }
}